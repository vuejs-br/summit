      O Vue.js Summit e todas as conferências filiadas são dedicadas a fornecer uma experiência de conferência livre de assédio para todos, independentemente do sexo, identidade de gênero e expressão, idade, orientação sexual, deficiência, aparência física, tamanho corporal, cor de pele, etnia, religião (ou falta dela) ou escolhas de tecnologias.

